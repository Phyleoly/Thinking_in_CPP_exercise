#include <iostream>

int main() {
    double a = 10.5;
    double b = 3.2;

    double sum = a + b;

    double difference = a - b;

    double product = a * b;

    double quotient = a / b;

    std::cout << "a + b = " << sum << std::endl;
    std::cout << "a - b = " << difference << std::endl;
    std::cout << "a * b = " << product << std::endl;
    std::cout << "a / b = " << quotient << std::endl;

    return 0;
}
